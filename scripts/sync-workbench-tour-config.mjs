/**
 * Writes public/workbench-demo/tour-config.js from workbenchDemoTour.json
 * so the iframe demo and Next.js frame share one tour source.
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const jsonPath = join(
  root,
  'src/components/workbench-demo/workbenchDemoTour.json'
)
const outPath = join(root, 'public/workbench-demo/tour-config.js')

const tour = JSON.parse(readFileSync(jsonPath, 'utf8'))

if (!tour.total || !Array.isArray(tour.steps) || tour.steps.length !== tour.total) {
  console.error('workbenchDemoTour.json: total must match steps.length')
  process.exit(1)
}

const body = `/* Generated by scripts/sync-workbench-tour-config.mjs — do not edit by hand. */
/* Source: src/components/workbench-demo/workbenchDemoTour.json */
window.WORKBENCH_DEMO_TOUR = ${JSON.stringify(tour, null, 2)};
`

writeFileSync(outPath, body, 'utf8')

const roundTrip = readFileSync(outPath, 'utf8')
if (!roundTrip.includes(JSON.stringify(tour.steps[0].srCaption).slice(1, -1))) {
  console.error('tour-config.js verification failed')
  process.exit(1)
}

console.log('Wrote', outPath)
